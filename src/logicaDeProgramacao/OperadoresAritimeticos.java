package logicaDeProgramacao;

public class OperadoresAritimeticos {

	public static void main(String[] args) {
		int numero = 10;
		System.out.println("Número = " + numero);
		System.out.println("Número = " + numero + " + 5 | Número " + (numero + 5));
		System.out.println("Número = " + numero + " - 5 | Número " + (numero - 5));
		System.out.println("Número = " + numero + " * 5 | Número " + (numero * 5));
		System.out.println("Número = " + numero + " / 5 | Número " + (numero / 5));
		System.out.println("Número = " + numero + " % 5 | Número " + (numero % 5));
	}

}
